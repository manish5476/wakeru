  /**
   * Get spending analytics for a period.
   */
  static async getAnalytics(userId: string, filters: AnalyticsFilters = {}) {
    let startDate: Date;
    let endDate: Date = new Date();

    switch (filters.period) {
      case 'week':
        startDate = new Date(Date.now() - 7 * 86400000);
        break;
      case 'month':
        startDate = new Date(Date.now() - 30 * 86400000);
        break;
      case 'quarter':
        startDate = new Date(Date.now() - 90 * 86400000);
        break;
      case 'year':
        startDate = new Date(Date.now() - 365 * 86400000);
        break;
      case 'all':
        startDate = new Date(0);
        break;
      default:
        startDate = filters.startDate ? new Date(filters.startDate) : new Date(Date.now() - 30 * 86400000);
        endDate = filters.endDate ? new Date(filters.endDate) : new Date();
    }

    const transactions = await Transaction.find({
      userId,
      date: { $gte: startDate, $lte: endDate },
      isDeleted: false,
      type: { $in: ['expense', 'trip_expense'] },
    }).sort({ date: 1 }).lean();

    // Daily spending trend
    const dailyTrend: Record<string, number> = {};
    transactions.forEach(t => {
      const key = new Date(t.date).toISOString().split('T')[0];
      dailyTrend[key] = (dailyTrend[key] || 0) + t.amount;
    });

    // Category distribution
    const categoryDistribution: Record<string, number> = {};
    transactions.forEach(t => {
      categoryDistribution[t.category] = (categoryDistribution[t.category] || 0) + t.amount;
    });

    const totalSpent = transactions.reduce((s, t) => s + t.amount, 0);

    // Payment method distribution
    const paymentMethods: Record<string, number> = {};
    transactions.forEach(t => {
      const method = t.paymentMethod || 'Other';
      paymentMethods[method] = (paymentMethods[method] || 0) + t.amount;
    });

    // Trip vs Manual breakdown
    const tripExpenseTotal = transactions
      .filter(t => t.type === 'trip_expense')
      .reduce((s, t) => s + t.amount, 0);
    const manualExpenseTotal = transactions
      .filter(t => t.type === 'expense')
      .reduce((s, t) => s + t.amount, 0);

    // Monthly trend
    const monthlyTrend: Record<string, number> = {};
    transactions.forEach(t => {
      const key = new Date(t.date).toISOString().substring(0, 7);
      monthlyTrend[key] = (monthlyTrend[key] || 0) + t.amount;
    });

    return {
      period: {
        startDate,
        endDate,
        days: Math.ceil((endDate.getTime() - startDate.getTime()) / 86400000),
      },
      overview: {
        totalSpent: Math.round(totalSpent * 100) / 100,
        averagePerTransaction: transactions.length > 0
          ? Math.round((totalSpent / transactions.length) * 100) / 100
          : 0,
        transactionCount: transactions.length,
        averageDaily: Math.round((totalSpent / Math.max(1, Math.ceil((endDate.getTime() - startDate.getTime()) / 86400000))) * 100) / 100,
      },
      dailyTrend: Object.entries(dailyTrend).map(([date, amount]) => ({
        date,
        amount: Math.round(amount * 100) / 100,
      })).sort((a, b) => a.date.localeCompare(b.date)),
      monthlyTrend: Object.entries(monthlyTrend).map(([month, amount]) => ({
        month,
        amount: Math.round(amount * 100) / 100,
      })).sort((a, b) => a.month.localeCompare(b.month)),
      categoryDistribution: Object.entries(categoryDistribution)
        .map(([category, amount]) => ({
          category,
          amount: Math.round(amount * 100) / 100,
          percent: totalSpent > 0 ? Math.round((amount / totalSpent) * 100) : 0,
        }))
        .sort((a, b) => b.amount - a.amount),
      paymentMethods: Object.entries(paymentMethods)
        .map(([method, amount]) => ({
          method,
          amount: Math.round(amount * 100) / 100,
          percent: totalSpent > 0 ? Math.round((amount / totalSpent) * 100) : 0,
        }))
        .sort((a, b) => b.amount - a.amount),
      tripVsManual: {
        tripExpenses: Math.round(tripExpenseTotal * 100) / 100,
        manualExpenses: Math.round(manualExpenseTotal * 100) / 100,
        tripPercent: totalSpent > 0 ? Math.round((tripExpenseTotal / totalSpent) * 100) : 0,
        manualPercent: totalSpent > 0 ? Math.round((manualExpenseTotal / totalSpent) * 100) : 0,
      },
    };
  }
