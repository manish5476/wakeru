  /**
   * Sync all trip expenses for a user into their personal finance transactions.
   * This ensures the finance dashboard shows ALL spending, including trip splits.
   */
  static async syncTripExpenses(userId: string): Promise<{ synced: number; skipped: number }> {
    // Get all expenses where user is involved (paid or split)
    const expenses = await Expense.find({
      $or: [
        { paidBy: userId },
        { 'splits.userId': userId },
      ],
      isArchived: false,
    }).lean();

    let synced = 0;
    let skipped = 0;

    for (const expense of expenses) {
      // Check if already synced
      const existing = await Transaction.findOne({
        userId,
        expenseId: expense._id,
        isAutoGenerated: true,
      });

      if (existing) {
        // Update if amounts changed
        const mySplit = expense.splits.find((s: any) => s.userId === userId);
        const myAmount = expense.paidBy === userId
          ? expense.amountBase - (expense.splits.filter((s: any) => s.isPaid && s.userId !== userId).reduce((sum: number, s: any) => sum + s.amountBase, 0))
          : (mySplit?.amountBase || 0);

        if (existing.amount !== myAmount || existing.myShare !== mySplit?.amountBase) {
          existing.amount = myAmount;
          existing.myShare = mySplit?.amountBase || 0;
          existing.totalExpenseAmount = expense.amountBase;
          await existing.save();
          synced++;
        } else {
          skipped++;
        }
        continue;
      }

      // Get trip name
      const trip = await Trip.findById(expense.tripId).select('title').lean();
      const mySplit = expense.splits.find((s: any) => s.userId === userId);
      const splitNames = expense.splits
        .filter((s: any) => s.userId !== userId)
        .map((s: any) => s.displayName);

      // Determine transaction type and amount
      let type: ITransaction['type'];
      let amount: number;

      if (expense.paidBy === userId) {
        type = 'trip_expense';
        // Total paid minus what others already paid back
        const paidBack = expense.splits
          .filter((s: any) => s.isPaid && s.userId !== userId)
          .reduce((sum: number, s: any) => sum + s.amountBase, 0);
        amount = expense.amountBase - paidBack;
      } else {
        type = 'trip_expense';
        amount = mySplit?.amountBase || 0;
      }

      const transaction = new Transaction({
        userId,
        type,
        amount,
        currency: expense.baseCurrency,
        title: expense.title,
        category: expense.category,
        date: expense.date,
        paymentMethod: 'Other',
        notes: expense.notes,
        receiptImage: expense.receiptImages?.[0],
        tags: expense.tags || [],
        tripId: expense.tripId,
        tripName: trip?.title,
        expenseId: expense._id,
        isAutoGenerated: true,
        myShare: mySplit?.amountBase || 0,
        totalExpenseAmount: expense.amountBase,
        splitWith: splitNames,
        location: expense.location,
      });

      await transaction.save();
      synced++;
    }

    // Also sync settlement payments
    await this.syncSettlementTransactions(userId);

    logger.info(`Trip expenses synced for user ${userId}: ${synced} new, ${skipped} updated`);
    return { synced, skipped };
  }

  /**
   * Sync settlement payments as finance transactions.
   */
  static async syncSettlementTransactions(userId: string): Promise<void> {
    const settlements = await Settlement.find({
      $or: [
        { 'transactions.from': userId },
        { 'transactions.to': userId },
      ],
    }).lean();

    for (const settlement of settlements) {
      for (const txn of settlement.transactions) {
        if (txn.status !== 'confirmed') continue;

        // Check if already synced
        const existing = await Transaction.findOne({
          userId,
          settlementId: settlement._id,
          title: { $regex: new RegExp(`settlement.*${txn.fromName}.*${txn.toName}`, 'i') },
        });

        if (existing) continue;

        if (txn.from === userId) {
          // User paid someone
          await Transaction.create({
            userId,
            type: 'settlement_paid',
            amount: txn.amountBase,
            currency: txn.baseCurrency,
            title: `Settlement: Paid ${txn.toName}`,
            category: 'settlement',
            date: txn.confirmedAt || new Date(),
            paymentMethod: 'UPI',
            notes: `Trip settlement payment to ${txn.toName}`,
            settlementId: settlement._id,
            isAutoGenerated: true,
          });
        }

        if (txn.to === userId) {
          // User received payment
          await Transaction.create({
            userId,
            type: 'settlement_received',
            amount: txn.amountBase,
            currency: txn.baseCurrency,
            title: `Settlement: Received from ${txn.fromName}`,
            category: 'settlement',
            date: txn.confirmedAt || new Date(),
            paymentMethod: 'UPI',
            notes: `Trip settlement received from ${txn.fromName}`,
            settlementId: settlement._id,
            isAutoGenerated: true,
          });
        }
      }
    }
  }

