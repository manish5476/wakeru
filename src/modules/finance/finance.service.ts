  static async setBudget(
    userId: string,
    month: string,
    totalBudget: number,
    categoryBudgets: { category: string; amount: number; color?: string }[] = []
  ) {
    // Calculate already spent for each category
    const startDate = new Date(`${month}-01T00:00:00.000Z`);
    const endDate = new Date(startDate.getFullYear(), startDate.getMonth() + 1, 0, 23, 59, 59, 999);

    const transactions = await Transaction.find({
      userId,
      type: { $in: ['expense', 'trip_expense'] },
      date: { $gte: startDate, $lte: endDate },
      isDeleted: false,
    });

    const categorySpent: Record<string, number> = {};
    let totalSpent = 0;
    transactions.forEach(t => {
      categorySpent[t.category] = (categorySpent[t.category] || 0) + t.amount;
      totalSpent += t.amount;
    });

    const categoryBudgetsWithSpent = categoryBudgets.map(cb => ({
      category: cb.category,
      amount: cb.amount,
      spent: categorySpent[cb.category] || 0,
      color: cb.color,
    }));

    const budget = await Budget.findOneAndUpdate(
      { userId, month },
      {
        totalBudget,
        totalSpent,
        categoryBudgets: categoryBudgetsWithSpent,
      },
      { upsert: true, new: true }
    );

    return budget;
  }

  static async getBudget(userId: string, month: string, precomputedTotal?: number) {
    let budget = await Budget.findOne({ userId, month });

    // Use precomputed total when provided (avoids redundant aggregate from dashboard)
    const totalSpent = precomputedTotal !== undefined
      ? precomputedTotal
      : await this.getTotalExpenseForMonth(userId, month);

    if (!budget) {
      budget = await Budget.create({
        userId,
        month,
        totalBudget: 0,
        totalSpent,
        categoryBudgets: [],
      });
    } else if (budget.totalSpent !== totalSpent) {
      budget.totalSpent = totalSpent;
      await budget.save();
    }

    return budget;
  }

  static async updateBudgetSpent(userId: string, month: string, category: string, amountDelta: number) {
    if (amountDelta === 0) return;

    const budget = await Budget.findOne({ userId, month });
    if (!budget) return;

    // Update category spent
    const catBudget = budget.categoryBudgets.find(cb => cb.category === category);
    if (catBudget) {
      catBudget.spent = Math.max(0, (catBudget.spent || 0) + amountDelta);
    }

    // Update total spent
    budget.totalSpent = Math.max(0, (budget.totalSpent || 0) + amountDelta);
    await budget.save();
  }

  static async getTotalExpenseForMonth(userId: string, month: string): Promise<number> {
    const startDate = new Date(`${month}-01T00:00:00.000Z`);
    const endDate = new Date(startDate.getFullYear(), startDate.getMonth() + 1, 0, 23, 59, 59, 999);

    const result = await Transaction.aggregate([
      {
        $match: {
          userId,
          type: { $in: ['expense', 'trip_expense', 'settlement_paid'] },
          date: { $gte: startDate, $lte: endDate },
          isDeleted: false,
        },
      },
      { $group: { _id: null, total: { $sum: '$amount' } } },
    ]);

    return result[0]?.total || 0;
  }

