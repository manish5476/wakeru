  static async markBillPaid(userId: string, billId: string, paidAmount?: number) {
    const bill = await Bill.findOne({ _id: billId, userId });
    if (!bill) throw new AppError('Bill not found', 404);

    bill.isPaid = true;
    bill.paidThisMonth = true;
    bill.paidAmount = paidAmount || bill.amount;
    bill.lastPaidDate = new Date();

    // Calculate next due date
    const nextDate = new Date(bill.dueDate);
    switch (bill.frequency) {
      case 'monthly': nextDate.setMonth(nextDate.getMonth() + 1); break;
      case 'weekly': nextDate.setDate(nextDate.getDate() + 7); break;
      case 'yearly': nextDate.setFullYear(nextDate.getFullYear() + 1); break;
    }
    bill.dueDate = nextDate;
    bill.nextDueDate = nextDate;
    bill.isPaid = false;
    bill.paidThisMonth = false;

    await bill.save();

    // Optionally create a transaction for this payment
    await this.createTransaction(userId, {
      type: 'expense',
      amount: paidAmount || bill.amount,
      currency: bill.currency,
      title: `Bill: ${bill.title}`,
      category: bill.category,
      date: new Date(),
      paymentMethod: 'UPI',
      notes: `Bill payment - ${bill.title}`,
      isAutoGenerated: true,
    });

    return bill;
  }

