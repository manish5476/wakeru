  static async createTransaction(userId: string, data: Partial<ITransaction>) {
    const transaction = new Transaction({
      ...data,
      userId,
      date: data.date || new Date(),
      isAutoGenerated: false,
    });
    await transaction.save();

    // Update budget if expense
    if (data.type === 'expense' && data.category) {
      const month = (data.date || new Date()).toISOString().substring(0, 7);
      await this.updateBudgetSpent(userId, month, data.category, data.amount || 0);
    }

    logger.info(`Transaction created: ${userId} - ${data.title}`);
    return transaction;
  }

  static async getTransactions(userId: string, filters: TransactionFilters = {}) {
    const query: any = { userId, isDeleted: false };

    if (filters.type) query.type = filters.type;
    if (filters.category) query.category = filters.category;
    if (filters.tripId) query.tripId = new Types.ObjectId(filters.tripId);
    if (filters.tags && filters.tags.length > 0) query.tags = { $in: filters.tags };
    if (filters.search) {
      query.$or = [
        { title: { $regex: filters.search, $options: 'i' } },
        { notes: { $regex: filters.search, $options: 'i' } },
      ];
    }

    if (filters.month) {
      const startDate = new Date(`${filters.month}-01T00:00:00.000Z`);
      const endDate = new Date(startDate.getFullYear(), startDate.getMonth() + 1, 0, 23, 59, 59, 999);
      query.date = { $gte: startDate, $lte: endDate };
    } else if (filters.startDate || filters.endDate) {
      query.date = {};
      if (filters.startDate) query.date.$gte = new Date(filters.startDate);
      if (filters.endDate) query.date.$lte = new Date(filters.endDate);
    }

    const limit = Math.min(filters.limit || 50, 100);
    const page = Math.max(filters.page || 1, 1);
    const skip = (page - 1) * limit;
    const sortField = filters.sortBy || 'date';
    const sortOrder = filters.sortOrder === 'asc' ? 1 : -1;

    const [transactions, total, summary] = await Promise.all([
      Transaction.find(query)
        .sort({ [sortField]: sortOrder, createdAt: -1 })
        .skip(skip)
        .limit(limit)
        .populate('tripId', 'title coverImage')
        .lean(),
      Transaction.countDocuments(query),
      Transaction.aggregate([
        { $match: query },
        {
          $group: {
            _id: null,
            totalExpense: { $sum: { $cond: [{ $in: ['$type', ['expense', 'trip_expense', 'settlement_paid']] }, '$amount', 0] } },
            totalIncome: { $sum: { $cond: [{ $in: ['$type', ['income', 'settlement_received']] }, '$amount', 0] } },
          },
        },
      ]),
    ]);

    return {
      transactions,
      summary: {
        totalExpense: summary[0]?.totalExpense || 0,
        totalIncome: summary[0]?.totalIncome || 0,
        netAmount: (summary[0]?.totalIncome || 0) - (summary[0]?.totalExpense || 0),
      },
      pagination: {
        page,
        limit,
        total,
        totalPages: Math.ceil(total / limit),
        hasMore: skip + transactions.length < total,
      },
    };
  }

  static async getTransactionById(userId: string, transactionId: string) {
    const transaction = await Transaction.findOne({
      _id: transactionId,
      userId,
      isDeleted: false,
    }).populate('tripId', 'title coverImage');

    if (!transaction) throw new AppError('Transaction not found', 404);
    return transaction;
  }

  static async updateTransaction(userId: string, transactionId: string, data: Partial<ITransaction>) {
    const transaction = await Transaction.findOne({
      _id: transactionId,
      userId,
      isDeleted: false,
    });

    if (!transaction) throw new AppError('Transaction not found', 404);

    const oldAmount = transaction.amount;
    const oldCategory = transaction.category;
    const oldType = transaction.type;
    const oldDate = transaction.date;

    // Don't allow editing auto-generated transactions (sync from trips instead)
    if (transaction.isAutoGenerated && (data.amount || data.category)) {
      throw new AppError('Trip expenses are auto-managed. Update the expense in the trip instead.', 400);
    }

    Object.assign(transaction, data);
    await transaction.save();

    // Update budget if expense changed
    if (oldType === 'expense' || oldType === 'trip_expense') {
      const oldMonth = new Date(oldDate).toISOString().substring(0, 7);
      await this.updateBudgetSpent(userId, oldMonth, oldCategory, -oldAmount);
    }

    if ((data.type === 'expense' || transaction.type === 'expense') && transaction.category) {
      const newMonth = new Date(data.date || oldDate).toISOString().substring(0, 7);
      await this.updateBudgetSpent(userId, newMonth, transaction.category, data.amount || transaction.amount);
    }

    return transaction;
  }

  static async deleteTransaction(userId: string, transactionId: string, permanent: boolean = false) {
    const transaction = await Transaction.findOne({ _id: transactionId, userId });

    if (!transaction) throw new AppError('Transaction not found', 404);

    if (permanent) {
      // Reverse budget
      if ((transaction.type === 'expense' || transaction.type === 'trip_expense') && transaction.category) {
        const month = new Date(transaction.date).toISOString().substring(0, 7);
        await this.updateBudgetSpent(userId, month, transaction.category, -transaction.amount);
      }
      await transaction.deleteOne();
    } else {
      transaction.isDeleted = true;
      transaction.deletedAt = new Date();
      await transaction.save();
    }

    return { success: true, message: 'Transaction deleted successfully' };
  }

  static async restoreTransaction(userId: string, transactionId: string) {
    const transaction = await Transaction.findOne({
      _id: transactionId,
      userId,
      isDeleted: true,
    });

    if (!transaction) throw new AppError('Transaction not found', 404);

    transaction.isDeleted = false;
    transaction.deletedAt = undefined;
    await transaction.save();

    // Restore budget
    if ((transaction.type === 'expense' || transaction.type === 'trip_expense') && transaction.category) {
      const month = new Date(transaction.date).toISOString().substring(0, 7);
      await this.updateBudgetSpent(userId, month, transaction.category, transaction.amount);
    }

    return transaction;
  }

