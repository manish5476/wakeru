  /**
   * Sync all trip expenses for a user into their personal finance transactions.
   * This ensures the finance dashboard shows ALL spending, including trip splits.
   */
  static async syncTripExpenses(userId: string): Promise<{ synced: number; skipped: number }> {
    // Fetch all relevant expenses in one query
    const expenses = await Expense.find({
      $or: [
        { paidBy: userId },
        { 'splits.userId': userId },
      ],
      isArchived: false,
    }).lean();

    if (expenses.length === 0) {
      await this.syncSettlementTransactions(userId);
      return { synced: 0, skipped: 0 };
    }

    // ── Batch fetch: all existing synced transactions for these expenses in one query ──
    const allExpenseIds = expenses.map((e: any) => e._id);
    const existingTxns = await Transaction.find({
      userId,
      expenseId: { $in: allExpenseIds },
      isAutoGenerated: true,
    }).lean();

    // Build a Map for O(1) lookup: expenseId → transaction
    const existingMap = new Map<string, any>();
    existingTxns.forEach((t: any) => {
      existingMap.set(String(t.expenseId), t);
    });

    // ── Batch fetch: all distinct trips referenced by these expenses ──
    const tripIds = [...new Set(expenses.map((e: any) => String(e.tripId)).filter(Boolean))];
    const trips = await Trip.find({ _id: { $in: tripIds } }).select('title').lean();
    const tripMap = new Map<string, string>();
    trips.forEach((t: any) => tripMap.set(String(t._id), t.title));

    let synced = 0;
    let skipped = 0;
    const bulkOps: any[] = [];
    const updateOps: Array<() => Promise<void>> = [];

    for (const expense of expenses) {
      const mySplit = expense.splits.find((s: any) => s.userId === userId);
      const paidBack = expense.splits
        .filter((s: any) => s.isPaid && s.userId !== userId)
        .reduce((sum: number, s: any) => sum + s.amountBase, 0);
      const myAmount = expense.paidBy === userId
        ? expense.amountBase - paidBack
        : (mySplit?.amountBase || 0);

      const existing = existingMap.get(String(expense._id));

      if (existing) {
        // Only update if amounts changed
        if (existing.amount !== myAmount || existing.myShare !== (mySplit?.amountBase ?? 0)) {
          updateOps.push(() =>
            Transaction.updateOne(
              { _id: existing._id },
              { $set: { amount: myAmount, myShare: mySplit?.amountBase || 0, totalExpenseAmount: expense.amountBase } }
            ).then(() => void 0)
          );
          synced++;
        } else {
          skipped++;
        }
        continue;
      }

      // New transaction — prepare for bulk insert
      const splitNames = expense.splits
        .filter((s: any) => s.userId !== userId)
        .map((s: any) => s.displayName);

      bulkOps.push({
        insertOne: {
          document: {
            userId,
            type: 'trip_expense' as ITransaction['type'],
            amount: myAmount,
            currency: expense.baseCurrency,
            title: expense.title,
            category: expense.category,
            date: expense.date,
            paymentMethod: 'Other',
            notes: expense.notes,
            receiptImage: expense.receiptImages?.[0],
            tags: expense.tags || [],
            tripId: expense.tripId,
            tripName: tripMap.get(String(expense.tripId)),
            expenseId: expense._id,
            isAutoGenerated: true,
            myShare: mySplit?.amountBase || 0,
            totalExpenseAmount: expense.amountBase,
            splitWith: splitNames,
            location: expense.location,
            isDeleted: false,
          },
        },
      });
      synced++;
    }

    // Execute all writes in parallel
    await Promise.all([
      bulkOps.length > 0 ? Transaction.bulkWrite(bulkOps, { ordered: false }) : Promise.resolve(),
      ...updateOps.map(op => op()),
      this.syncSettlementTransactions(userId),
    ]);

    logger.info(`Trip expenses synced for user ${userId}: ${synced} new/updated, ${skipped} skipped`);
    return { synced, skipped };
  }

  /**
   * Sync settlement payments as finance transactions.
   */
  static async syncSettlementTransactions(userId: string): Promise<void> {
    const settlements = await Settlement.find({
      $or: [
        { 'transactions.from': userId },
        { 'transactions.to': userId },
      ],
    }).lean();

    if (settlements.length === 0) return;

    // Batch fetch all already-synced settlement transactions for this user
    const settlementIds = settlements.map((s: any) => s._id);
    const existingSettlementTxns = await Transaction.find({
      userId,
      settlementId: { $in: settlementIds },
      isAutoGenerated: true,
    }).select('settlementId title').lean();

    // Build a Set of already-synced keys: `${settlementId}:${title}`
    const syncedKeys = new Set<string>();
    existingSettlementTxns.forEach((t: any) => {
      syncedKeys.add(`${String(t.settlementId)}:${t.title}`);
    });

    const toCreate: any[] = [];

    for (const settlement of settlements) {
      for (const txn of (settlement as any).transactions) {
        if (txn.status !== 'confirmed') continue;

        if (txn.from === userId) {
          const title = `Settlement: Paid ${txn.toName}`;
          if (!syncedKeys.has(`${String(settlement._id)}:${title}`)) {
            toCreate.push({
              userId,
              type: 'settlement_paid',
              amount: txn.amountBase,
              currency: txn.baseCurrency,
              title,
              category: 'settlement',
              date: txn.confirmedAt || new Date(),
              paymentMethod: 'UPI',
              notes: `Trip settlement payment to ${txn.toName}`,
              settlementId: settlement._id,
              isAutoGenerated: true,
              isDeleted: false,
            });
          }
        }

        if (txn.to === userId) {
          const title = `Settlement: Received from ${txn.fromName}`;
          if (!syncedKeys.has(`${String(settlement._id)}:${title}`)) {
            toCreate.push({
              userId,
              type: 'settlement_received',
              amount: txn.amountBase,
              currency: txn.baseCurrency,
              title,
              category: 'settlement',
              date: txn.confirmedAt || new Date(),
              paymentMethod: 'UPI',
              notes: `Trip settlement received from ${txn.fromName}`,
              settlementId: settlement._id,
              isAutoGenerated: true,
              isDeleted: false,
            });
          }
        }
      }
    }

    if (toCreate.length > 0) {
      await Transaction.insertMany(toCreate, { ordered: false });
    }
  }

